    private void btnMenúActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMenúActionPerformed
        //System.out.println(lbTitulo.getBounds());
        if(Panel_Slider.getX() > -1){
            Animacion.Animacion.mover_izquierda(0, -264, 2, 2, Panel_Slider);
            MainPanel.setBounds(0, 0, 1110, 619);
            lbTitulo.setBounds(0, 8, 1110, 32);
            
            switch (PrincipalChanged) {
                case -1: 
                    jLabel4.setSize(1110, jLabel4.getHeight());
                    jLabel5.setSize(1110, jLabel5.getHeight());
                    jLabel9.setSize(1110, jLabel9.getHeight());
                    break;
                case 0: Lexico.RedimFull(true); break;
                case 1: Sintactico.RedimFull(true); break;
            }
        }else{
            switch (PrincipalChanged) {
                case -1: 
                    jLabel4.setSize(880, jLabel4.getHeight());
                    jLabel5.setSize(880, jLabel5.getHeight());
                    jLabel9.setSize(880, jLabel9.getHeight());
                    break;
                case 0: Lexico.RedimFull(false); break;
                case 1: Sintactico.RedimFull(true); break;
            }    
            Animacion.Animacion.mover_derecha(-264, 0, 2, 2, Panel_Slider);
            MainPanel.setBounds(230, 0, 880, 619);
            lbTitulo.setBounds(208, 8, 880, 32);
        }
    }//GEN-LAST:event_btnMenúActionPerformed

