    private void AjustarPanel()
    {
        resetColor(new JPanel[]{btn_1,btn_2,btn_3},
                new JPanel[]{ind_1,ind_2,ind_3});
    }
    
    private void AjustarPanel(JPanel PANEL)
    {
        Panel_Ayuda.setVisible(false);
        PANEL.setSize(880, 618);
        PANEL.setLocation(0, 0);
        resetColor(new JPanel[]{btn_1,btn_2,btn_3},
                new JPanel[]{ind_1,ind_2,ind_3});
        MainPanel.removeAll();
        MainPanel.add(PANEL,BorderLayout.CENTER);
        MainPanel.revalidate();
        MainPanel.repaint();
    }
    
     private void resetColor(JPanel [] pane, JPanel [] indicators)
    {
        for (JPanel pane1 : pane) {
            pane1.setBackground(new Color(23,35,51));
        }
        for (JPanel indicator : indicators) {
            indicator.setOpaque(false);           
        }
        
    }
    
    
    private void setColor(JPanel pane)
    {
        pane.setBackground(new Color(41,57,80));
    }

